package com.yusufwdn.javafundamental.operator;

public class Aritmethic {
    public static void main(String[] args) {
        System.out.println("== Operasi Penjumlahan ==");
        int hasilJumlah = 1 + 1;
        System.out.println("Hasil 1 + 1 = " + hasilJumlah);
        System.out.println();

        System.out.println("== Operasi Pengurangan ==");
        int hasilKurang = 3 - 1;
        System.out.println("Hasil 3 - 1 = " + hasilKurang);
        System.out.println();

        System.out.println("== Operasi Pengalian ==");
        int hasilKali = 10 * 5;
        System.out.println("Hasil 10 * 5 = " + hasilKali);
        System.out.println();

        System.out.println("== Operasi Pembagian ==");
        int hasilBagi = 28 / 7;
        System.out.println("Hasil 28 / 7 = " + hasilBagi);
        System.out.println();

        System.out.println("== Operasi Sisa Bagi ==");
        int sisaBagi = 33 % 3;
        int sisaBagiLagi = 11 % 2;
        System.out.println("Hasil 20 % 2 = " + sisaBagi);
        System.out.println("Hasil 11 % 2 = " + sisaBagiLagi);
        System.out.println();
    }
}
